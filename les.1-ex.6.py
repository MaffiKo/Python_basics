a = int(input("Введите количество км которые спортсмен преодолел в первый день "))
b = int(input("Неменее скольки км спортсмен хочет преодолеть "))
increase = 10  # (%)
day = 1

while a < b:
    if a < b:
        a = a + (a * increase / 100)
        day += 1
    else:
        break

print(f"На {day}-й день спортсмен достиг результата не менее - {b} км")