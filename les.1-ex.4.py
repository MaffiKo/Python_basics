number = int(input("Хотите узнать наибольшую цифру в числе? Введите целое положительное число: "))
a = number % 10
b = number // 10

while b > a:
    if b % 10 > a:
        a = b % 10
        b = b // 10
    else:
        break

print(f"Наибольшей цифрой является {a}")


